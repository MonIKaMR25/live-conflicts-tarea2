def suma(n1: int, n2: int) -> int:
    """Operación de suma"""
    return n1 + n2 

def resta(n1: int, n2: int) -> int:
    """Operación de resta"""
    return n1 - n2

def multiplica(n1: int, n2: int) -> int:
    """Operación de multiplicación"""
    return n1 * n2

def divide(n1: int, n2: int) -> float:
    """Operación de división"""
    return n1 / n2

